package com.fastcampus.ch07;
//07. 자료형(data type) - 실수는 어떻게 표현하나요?
public class DoubleTest {
    public static void main(String[] args) {
        double dnum = 3.14; //기본적으로 실수를 사용할땐 double를 사용한다.
        float fnum = 3.14F; //4 byte인 float 타입일시 맨 뒤에 F를 붙인다.

        System.out.println(dnum); // 3.14
        System.out.println(fnum); // 3.14
    }
}
